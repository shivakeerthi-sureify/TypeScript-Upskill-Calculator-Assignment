export function addition (number_1 : number , number_2 : number) : number {

    return number_1 + number_2
}
export function substraction (number_1 : number , number_2 : number) : number {

    return number_1 - number_2
}
export function multiplication (number_1 : number , number_2 : number) : number {

    return number_1 * number_2
}
export function division (number_1 : number , number_2 : number) : number {

    return number_1 / number_2
}

